var _TS_Localize_Game_Interpreter_setupChoices = Game_Interpreter.prototype.setupChoices;
Game_Interpreter.prototype.setupChoices = function(params) {
	var c_text = ' ||| ';
	
	var new_param = params.clone();
	var choices = new_param[0].clone();
	
	
	for (var i = 0; i < choices.length; i++) {
		if(choices[i].indexOf(c_text) != -1){
			var text = choices[i].split(c_text);
			choices[i] = text[ConfigManager.langSelect];
		}
	}
	
	new_param[0] = choices;
	
	_TS_Localize_Game_Interpreter_setupChoices.call(this, new_param);
}


